// Process subscription form submissions
function process_subscription_form() {
    global $wpdb;

    if (isset($_POST['subscribe'])) {
        $user_email = sanitize_email($_POST['user_email']);
        $product_id = absint($_POST['product_id']);

        // Check if the user is already subscribed to the product
        $existing_subscription = $wpdb->get_var(
            $wpdb->prepare(
                "SELECT COUNT(*) FROM {$wpdb->prefix}price_drop_subscribers WHERE user_email = %s AND product_id = %d",
                $user_email,
                $product_id
            )
        );

        if (!$existing_subscription) {
            // Insert the new subscription into the database
            $wpdb->insert(
                $wpdb->prefix . 'price_drop_subscribers',
                array(
                    'user_email' => $user_email,
                    'product_id' => $product_id,
                ),
                array('%s', '%d')
            );
        }
    }
}

add_action('init', 'process_subscription_form');


// Function to check for price drops and send notifications
function check_price_drops() {
    global $wpdb;

    // Get all subscribers
    $subscribers = $wpdb->get_results("SELECT * FROM {$wpdb->prefix}price_drop_subscribers");

    foreach ($subscribers as $subscriber) {
        $user_email = $subscriber->user_email;
        $product_id = $subscriber->product_id;

        // Get the current and previous prices of the product
        $current_price = get_post_meta($product_id, '_price', true);
        $previous_price = get_post_meta($product_id, '_price_before_discount', true);

        // Check if the current price is less than the previous price
        if ($current_price < $previous_price) {
            // Send email notification to the subscriber
            $subject = 'Price Drop Notification';
            $message = "The price of the product with ID $product_id has dropped. Current price: $current_price";

            $headers[] = 'Content-Type: text/html; charset=UTF-8';

            wp_mail($user_email, $subject, $message, $headers);
        }
    }
}

// Schedule the price drop check to run daily
function schedule_price_drop_check() {
    if (!wp_next_scheduled('check_price_drops_event')) {
        wp_schedule_event(time(), 'daily', 'check_price_drops_event');
    }
}

add_action('init', 'schedule_price_drop_check');
add_action('check_price_drops_event', 'check_price_drops');







